package lesson7;

public class Cat {

    String nickname;
    String animal;
    int age;
    int running;
    double weight;
    int swimming;

    public Cat(String nickname, String animal, int age, int running,
               double weight, int swimming) {
        this.nickname = nickname;
        this.animal = animal;
        this.age = age;
        this.running = running;
        this.weight = weight;
        this.swimming = swimming;
    }


}
